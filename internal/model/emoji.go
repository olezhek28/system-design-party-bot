func GetEmoji(emojiSet ...string) string {
	var set string
	if len(emojiSet) == 0 {
		set = DefaultEmojis
	}

	builder := strings.Builder{}
	for _, s := range emojiSet {
		builder.WriteString(s)
	}

	set = builder.String()

	rand.Seed(time.Now().UnixNano())
	return string([]rune(set)[rand.Intn(utf8.RuneCountInString(set))])
}
